export const gameRegistry = cognitiveDomains.flatMap((domain) =>
  domain.games.map((registeredGame) => ({
    ...registeredGame,
    domain: domain.id,
    domainName: domain.name,
  })),
);

export function getRegisteredGamesForDomain(domainId: CognitiveDomainId) {
  return gameRegistry.filter((registeredGame) => registeredGame.domain === domainId);
}
