import type { CognitiveDomain, CognitiveDomainId, CognitiveGame, GameDifficulty } from "../types/cognition";

type RegisteredGame = Omit<CognitiveGame, "playRoute" | "implemented" | "locked"> & {
  implemented?: boolean;
  locked?: boolean;
};

function playRoute(id: string) {
  return `/games/${id}`;
}

function game(definition: RegisteredGame): CognitiveGame {
  return {
    ...definition,
    playRoute: playRoute(definition.id),
    implemented: definition.implemented ?? true,
    locked: definition.locked ?? false,
  };
}

function variables(...items: string[]) {
  return items;
}

function instructions(...items: string[]) {
  return items;
}

function domainScoreMetrics(...items: string[]) {
  return items;
}

function baseGame({
  id,
  name,
  description,
  fullDescription,
  difficulty,
  estimatedTime,
  cognitiveVariables,
  exampleTask,
  recommendedSkillLevel,
  subdomainIds,
  primarySkill,
  metrics,
  instructions: steps,
  playableGameType,
}: {
  id: string;
  name: string;
  description: string;
  fullDescription: string;
  difficulty: GameDifficulty;
  estimatedTime: string;
  cognitiveVariables: string[];
  exampleTask: string;
  recommendedSkillLevel: string;
  subdomainIds: string[];
  primarySkill: string;
  metrics: string[];
  instructions: string[];
  playableGameType?: CognitiveGame["playableGameType"];
}) {
  return game({
    id,
    name,
    description,
    fullDescription,
    instructions: steps,
    difficulty,
    estimatedTime,
    cognitiveVariables,
    exampleTask,
    recommendedSkillLevel,
    subdomainIds,
    primarySkill,
    metrics,
    playableGameType,
  });
}

